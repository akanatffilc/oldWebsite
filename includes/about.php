				<h1>略歴</h1>
				<div class="img-placeholder"></div>
				<p>サンフランシスコ在住のソフトウェア/ウェブ開発者です。</p>
				<p>サンフランシスコ近辺の通称ベイエリアで生まれ育ち、日本語と英語のバイリンガルです。高校ではスポーツに熱中し、4年間で６つの異なるスポーツのチームに参加しました。</p>
				<p>高校卒業後、オレンジカウンティ（LAから南に1時間）にあるアメリカ創価大学へ進学し、国際学を専攻。 卒論ではナイジェリアの初等教育においてテクノロジーがどのように使われ、それが生徒にどのような影響を及ぼすかについての考察をまとめました。 学生時代の一番の思い出は、半年間中国へ留学したことで、異なる文化や習慣を持つ友人たちと中国語を使って生活する体験は、自分の中でお金では買えない価値があり、自分の世界観を大きく広げてくれました。</p>
				<p>教育への関心は卒論だけにとどまらず、大学卒業後にベイエリアのイーストベイ地域で中学、高校の生徒の指導者として、勉強が楽しくなるように工夫がされたプログラムを作成する仕事に携わりました。その組織で1年間働いた後、教育現場に立つだけではなく、卒論にも書いた教育を補完するテクノロジーについての見識を深めるため、コンピュータサイエンスの勉強を始め、結果的にYahoo!で仕事を得ることができました。その後も勉強を続け、1年半Yahoo!で働いた後、教育工学のスタートアップ企業で仕事する機会を得ました。現在その会社で幼稚園から中学生向けのオンライン算数ゲームの開発をしてます。プログラミング以外にもマーケンティング戦略などの構成も手がけてます。</p>
